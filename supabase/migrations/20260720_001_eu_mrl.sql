GRANT USAGE, SELECT ON SEQUENCE qms.eu_mrl_id_seq          TO authenticated, service_role;
GRANT USAGE, SELECT ON SEQUENCE qms.eu_mrl_sync_log_id_seq TO authenticated, service_role;
